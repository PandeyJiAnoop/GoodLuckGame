package com.akp.aonestar.GoodLuckModule;

public class OfferAdapter {
    private String Discount,Photo;

    public OfferAdapter(String Discount,String Photo) {
        this.Discount = Discount;
        this.Photo = Photo;
    }

    public String getDiscount() {
        return Discount;
    }

    public String setDiscount() {
        return Discount;
    }


    public String getPhoto() {
        return Photo;
    }

    public String setPhoto() {
        return Photo;
    }
}
